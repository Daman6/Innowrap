package com.example.innowrapassignment;

public class PostItem {

    String title;
    int image;

    public PostItem() {
    }

    public String getTitle() {
        return title;
    }

    public int getImage() {
        return image;
    }

    public PostItem(String title, int image) {
        this.title = title;
        this.image = image;
    }
}
